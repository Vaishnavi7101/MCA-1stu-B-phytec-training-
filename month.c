#include<stdio.h>
int main()
{
	int months=3;
	switch (months)
	{
		case 1:
			printf("january\n");
break;
		case 2:
			printf("febuary\n");
break;
		case 3:
			printf("march\n");
break;
		case 4:
			printf("april\n");
break;
		case 5:
			printf("may\n");
break;
		case 6:
			printf("june\n");
break;
		case 7:
			printf("july\n");
break;
		default:
			printf("invalid months\n");
break;
	}
	return 0;
}



